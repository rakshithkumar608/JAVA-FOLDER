public class variables {
    public static void main(String[] args) {
        // Declare a variable
        int myNumber = 5;

        // Print the variable
        System.out.println("My number is: " + myNumber);

        // Change the value of the variable
        myNumber = 10;

        // Print the new value of the variable
        System.out.println("My number is now: " + myNumber);

        // Declare a constant
        final int MY_CONSTANT = 100;

        // Print the constant
        System.out.println("My constant is: " + MY_CONSTANT);
    }
}
